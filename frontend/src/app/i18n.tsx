type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
  dataText: (value: string | null | undefined) => string;
  statusLabel: (status: string) => string;
  formatDateTime: (value: string) => string;
  formatDate: (value: string) => string;
  formatNumber: (value: number) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

const intlLocale = (locale: Locale) => locale === "de" ? "de-DE" : "en-GB";

function applyParams(text: string, params?: Record<string, string | number>) {
  if (!params) return text;
  return Object.entries(params).reduce((current, [key, value]) => current.replaceAll(`{${key}}`, String(value)), text);
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("de");

  useEffect(() => {
    const stored = window.localStorage.getItem("cloud-wms-locale") as Locale | null;
    if (stored === "de" || stored === "en") setLocaleState(stored);
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale;
    window.localStorage.setItem("cloud-wms-locale", locale);
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: string, params?: Record<string, string | number>) => applyParams(dictionaries[locale][key] || dictionaries.en[key] || key, params);
    const dataText = (input: string | null | undefined) => {
      if (!input) return "";
      return dataTranslations[input]?.[locale] || input;
    };
    const statusLabel = (status: string) => t(`status.${status}`);
    const formatDateTime = (dateValue: string) => new Intl.DateTimeFormat(intlLocale(locale), {
      day: "2-digit",
      month: "short",
      hour: "2-digit",
      minute: "2-digit"
    }).format(new Date(dateValue));
    const formatDate = (dateValue: string) => new Intl.DateTimeFormat(intlLocale(locale), {
      day: "2-digit",
      month: "short",
      year: "numeric"
    }).format(new Date(dateValue));
    const formatNumber = (numberValue: number) => new Intl.NumberFormat(intlLocale(locale)).format(numberValue);

    return { locale, setLocale: setLocaleState, t, dataText, statusLabel, formatDateTime, formatDate, formatNumber };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error("useI18n must be used inside I18nProvider");
  return context;
}
